package loops;

public class UnderstandingLoops {
    public static void main(String[] args) {

        int year = 2022;
        for (int i = 1; i <= 35; i++) {
            System.out.println("The age is = " + i + " in " + year);
            year++;
        }
        System.out.println("End of the program!");
    }
}
